    	驾校简介
<p>东方时尚驾驶学校股份有限公司目前的主营业务是驾驶员培训。公司占地面积2000余亩，拥有世界一流的训练场及配套设施，现有宝来、桑塔纳、伊兰特、爱丽舍和金龙大型客车、中型客车、大型货车、牵引车、摩托车等各种训练用车2000部，员工3000余人，总资产达数亿元。</p>
 

<p>多年来，公司始终坚持把向社会输送合格的驾驶人作为己任，把学员是否满意作为工作的唯一标准，独树一帜地创立了“朋友式的教学关系”、“我们的服务与您的需求同步”、“三级服务链”等全新理念，形成了“服务、遵章、团结、自律、创新”促进公司建设与发展的十字方针，特别是围绕“让每位学员都满意”的工作追求，率先在行业内提出了杜绝行业不正之风、维护学员利益的“五项承诺”，以自己独特的经营理念和管理模式成为“开创全新理念的先锋，引领驾校行业的脚步”之先驱，为提升整个驾驶人培训行业的从业者素质和管理水平积累了宝贵的经验，为建设和谐交通做出了应有的贡献。 </p>

<img src="/drivingSchool/Public/image/knowlege1.jpg" alt="" width='500' >


<p>公司先后荣获2013年1月中国保护消费者基金会颁发的《维护消费者权益诚信服务满意单位》、《保护消费者权益3 15放心消费单位》称号；2012年7月北京市公安局公安交通管理局车辆管理所颁发的《北京市安全文明驾校》称号；2012年1月中国合作贸易企业协会、中国企业改革与发展研究会、中国企业信用评价中心颁发的《中国AAA级信用企业》、《2011年度全国文明诚信示范单位》称号；2011年1月新京报颁发的《我最信赖的驾驶培训品牌》称号。2009年，公司承担了为国庆60周年群众游行彩车培训驾驶员的光荣政治任务，荣获首都国庆60周年群众游行指挥部颁发的“首都国庆60周年群众游行支持贡献单位”荣誉称号。</p>

